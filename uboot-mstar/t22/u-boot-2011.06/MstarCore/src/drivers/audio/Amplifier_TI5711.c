MAPI_BOOL Amplifier_TI5711_StandBy(MAPI_U8 u8Para)
{
    if(u8Para == FALSE)
    {
        Audio_Amplifier_OFF();
    }
    else
    {
        Audio_Amplifier_ON();
    }
    return TRUE;
}

MAPI_BOOL Amplifier_TI5711_Reset(MAPI_U8 u8Para)
{
    if(u8Para == FALSE)
    {
        Audio_Amplifier_RST_OFF();
    }
    else
    {
        Audio_Amplifier_RST_ON();
    }
    return TRUE;
}

