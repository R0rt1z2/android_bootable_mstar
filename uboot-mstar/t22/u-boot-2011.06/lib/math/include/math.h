#ifndef _MATH_H_
#define	_MATH_H_

#define	isfinite(x)					\
    ((sizeof (x) == sizeof (float)) ? __isfinitef(x)	\
    : (sizeof (x) == sizeof (double)) ? __isfinite(x)	\
    : __isfinitel(x))

int  __isfinite(double);
int  __isfinitef(float);
int  __isfinitel(long double);

double	floor(double);
double	round(double);

#endif /* _MATH_H_ */
